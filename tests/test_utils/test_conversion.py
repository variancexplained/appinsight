        converter = ToPandas()
        df = converter.convert(data=spark_dataframe, viaspark=True)
        assert isinstance(df, pd.DataFrame)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

