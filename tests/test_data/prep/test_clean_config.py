import inspect
import logging
from datetime import datetime

import pandas as pd
import pytest

from appinsight.data_prep.clean import CleanConfig

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.config
class TestCleanConfig:  # pragma: no cover
    # ============================================================================================ #
    def test_clean_config(self, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        config = CleanConfig(
            name="TestCleanConfig", impact_to_remove=["Critical", "High"]
        )
        assert isinstance(config.config, pd.DataFrame)
        assert isinstance(config.get_issues_to_remove(), list)
        logging.debug(config.config)
        logging.debug(config.get_issues_to_remove())
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
