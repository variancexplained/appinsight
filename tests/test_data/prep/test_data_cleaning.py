import inspect
import logging
from datetime import datetime

import pytest

from appinsight.data_prep.clean import CleanConfig, DataCleaner

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.clean
class TestDataCleaningTask:  # pragma: no cover
    # ============================================================================================ #
    def test_clean(self, data_dqa, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        logging.info(data_dqa.info())
        config = CleanConfig(force=True)
        cleaner = DataCleaner(config=config)
        clean_df = cleaner.execute()
        assert len(clean_df) < len(data_dqa)  # issues removed.
        assert clean_df.shape[1] < data_dqa.shape[1]  # dqa columns removed

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_clean_endpoint_exists(self, data_dqa, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        config = CleanConfig(force=False)
        cleaner = DataCleaner(config=config)
        clean_df = cleaner.execute()
        assert len(clean_df) < len(data_dqa)  # issues removed.
        assert clean_df.shape[1] < data_dqa.shape[1]  # dqa columns removed

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
